class Solution:
    def isValidSudoku(self, board: list[list[str]]) -> bool:
        #字符串拼接，放到一个set里面，遍历一边即可
        st=set()
        for i in range(9):
            for j in range(9):
                n = board[i][j]
                if n=='.':continue
                row= n +"Row"+str(i)
                col= n +"COL"+str(j)
                block = n+"BLOCK"+str(i//3)+str(j//3)
                if st.__contains__(row) or st.__contains__(col) or st.__contains__(block):
                    return False
                else :
                    st.add(row)
                    st.add(col)
                    st.add(block)
        return True


